package testes;

public class Pato {
	private String name;
	private String cor;
	
	public Pato(String name, String cor) {
		super();
		this.name = name;
		this.cor = cor;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCor() {
		return cor;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}
	
	
}
